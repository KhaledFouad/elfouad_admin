class ExtraRow {
  final String id;
  final String name;
  final bool active;
  final String category;
  final double priceSell;
  final double costUnit;
  final double stockUnits;
  final String unit;

  const ExtraRow({
    required this.id,
    required this.name,
    required this.active,
    required this.category,
    required this.priceSell,
    required this.costUnit,
    required this.stockUnits,
    required this.unit,
  });
}
